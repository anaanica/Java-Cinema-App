    private void initValidation() {
        validationFields.put(tfUsername, lbUsernameError);
        validationFields.put(pfPassword, lbPasswordError);
    }

    private void initRepository() throws Exception {
        userRepository = RepositoryFactory.getUserRepository();
    }

    private boolean formValid() {
        boolean ok = true;

        for (Map.Entry<JTextComponent, JLabel> entry : validationFields.entrySet()) {
            JTextComponent textComponent = entry.getKey();
            JLabel errorLabel = entry.getValue();

            String text = textComponent.getText().trim();

            ok &= !text.isEmpty();
            errorLabel.setText(text.isEmpty() ? "X" : "");

            if (text.isEmpty()) {
                lbInfo.setText("Please enter username and password");
                textComponent.requestFocus();
            } else {
                lbInfo.setText("");
            }
        }

        return ok;
    }
