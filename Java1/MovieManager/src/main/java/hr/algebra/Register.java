    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        lbInfo.setText("");
        if (formValid()) {
            try {
                String username = tfUsername.getText().trim();
                String password = pfPassword.getText().trim();
                if (userRepository.userExists(username) == 1) {
                    lbInfo.setText("User already exists");

                    for (JTextComponent textComponent : validationFields.keySet()) {
                        textComponent.setText("");
                    }

                    tfUsername.grabFocus();
                } else {
                    newUserId = userRepository.register(username, password, userRoleType);
                    User user = new User(
                            newUserId,
                            tfUsername.getText().trim(),
                            pfPassword.getText().trim());

                    if (userAddable.addUser(user)) {
                        MessageUtils.showInformationMessage("Success", "User successfully registered");
                        dispose();
                    } else {
                        lbInfo.setText("Registration failed");
                    }
                }

            } catch (Exception ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

